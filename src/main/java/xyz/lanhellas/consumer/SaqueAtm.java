package xyz.lanhellas.consumer;

public class SaqueAtm {

	private String numeroAtm;
	private String conta;
	private String valorSacado;
	
	public String getNumeroAtm() {
		return numeroAtm;
	}
	public void setNumeroAtm(String numeroAtm) {
		this.numeroAtm = numeroAtm;
	}
	public String getConta() {
		return conta;
	}
	public void setConta(String conta) {
		this.conta = conta;
	}
	public String getValorSacado() {
		return valorSacado;
	}
	public void setValorSacado(String valorSacado) {
		this.valorSacado = valorSacado;
	}
	
}
